let num1 = parseInt(prompt(`digite um número`))
let num2 = parseInt(prompt(`digite outro número`))

if (num1==num2) {
    console.log(`os números são iguais`)
} else if (num1>num2) {
    console.log(`os números são diferentes, o número ${num1} é maior`)
} else {
    console.log(`os números são diferentes, o número ${num2} é maior`)
} 



